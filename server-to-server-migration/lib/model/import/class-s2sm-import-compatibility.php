class S2sm
_Import_Compatibility {

	public static function execute( $params ) {

		// Set progress
		S2sm
_Status::info( __( 'Checking extensions compatibility...', S2SM_PLUGIN_NAME ) );

		// Get messages
		$messages = S2sm
_Compatibility::get( $params );

		// Set messages
		if ( empty( $messages ) ) {
			return $params;
		}

		// Set progress
		S2sm
_Status::error( implode( $messages ) );

		exit;
	}
}
